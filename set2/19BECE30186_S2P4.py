# Set-2 Practical-4 : Write a Python program to check whether entered character is vowel or consonant.

a = input("Enter character : ")
if (a == 'a') | (a == 'e') | (a == 'i') | (a == 'o') | (a == 'u') | (a == 'A') | (a == 'E') | (a == 'I') | (a == 'O') | (a == 'U'):
    print("It is Vowel")
else:
    print("It is Consonant")
